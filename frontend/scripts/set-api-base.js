const fs = require('fs')
const path = require('path')

// Usage:
//   node scripts/set-api-base.js "https://script.google.com/macros/s/XXX/exec"
// or set env API_BASE and run without args

const arg = process.argv[2]
const apiBase = arg || process.env.API_BASE || ''

const target = path.resolve(__dirname, '..', 'src', 'config.js')
const content = `// Auto-generated by scripts/set-api-base.js\nexport const API_BASE = ${apiBase ? `'${apiBase}'` : `''`}\n`

fs.writeFileSync(target, content, 'utf8')
console.log(`Wrote API_BASE to ${target}: ${apiBase}`)
